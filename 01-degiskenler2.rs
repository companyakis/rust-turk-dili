#[allow(unused_variables)]


fn main() {

    //değiştirilebilir değişken tanımlayalım
    
    let mut yil: u16 = 1997;
    
    let dogum_yili: u16 = 1970;
    
    println!("Ali'nin yaşı: {}", yil - dogum_yili);
    
    yil = 2024;
    
    println!("Ali'nin yaşı: {}", yil - dogum_yili);
    
}


